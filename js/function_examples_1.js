"use strict";


function isLowerCase(letter){
    if (letter == letter.toLowerCase()){
        return true;
    }else{
        return false;
    }
}

function double(n){
    return n * 2;
}

function areIdentical(input1, input2){
    if (input1 === input2) {
        return true;
    }else{
        return false;
    }
}